#include<iostream>
#include<conio.h>
using namespace std;
int main()
{
int a[10][10],b[10][10],i,j,m,n,z,k,c,d,o,p,q;
cout<<"Enter rows and columns";
cin>>m>>n;
o=m;
p=n;
for(i=1;i<=m;i++)
{
for(j=1;j<=n;j++)
{
cin>>a[i][j];
}
}
if(m%2==0)
z=m/2;
if(n%2==0)
z=n/2;
k=1;
while(k<=z)
{
m=m-k+1;
n=n-k+1;
for(c=1;c<=m;c++)
{
for(d=1;d<=n;d++)
{
if(c==k&&d==k)
{
q=k;
while(q<n)
{
b[c][q]=a[c][q+1];
q++;
}
}
if(c==m&&d==k+1)
{
q=k+1;
while(q<=n)
{
b[c][q]=a[c][q-1];
q++;
}
}
if(d==k&&c==k)
{
q=k;
while(q<m)
{
b[q+1][d]=a[q][d];
q++;
}
}
if(d==n&&c==k)
{
q=k;
while(q<m)
{
b[q][d]=a[q+1][d];
q++;
}
}
}
}
k++;
}
for(i=1;i<=o;i++)
{
for(j=1;j<=p;j++)
{
cout<<" "<<b[i][j];
}
cout<<"\n";
}
getch();
return 0;
}
